package gr.aueb.cf.ch4;

public class PrintIntSumApp {

    public static void main(String[] args) {


        //Declaration and Initialization of Variables
        int sum = 0;

        for (int i = 1; i <= 50; i++) {
            sum += i;
        }

        //Output - Print out
        System.out.println(sum);


    }
}
